"""
Operadores en el lugar

Los operadores en el lugar le permiten escribir código como 'x = x + 3' de manera más concisa, como 'x + = 3'.
Lo mismo es posible con otros operadores como -, *, / y% también.
"""

print("-----------------------------------------\nOperadores en el lugar\n-----------------------------------------")

print()

x = 2
print(x)

x += 3
print(x)

print()

"""
Estos operadores también se pueden usar en tipos distintos de números, como cadenas.
"""

x = "spam"
print(x)

x += "eggs"
print(x)



"""
Booleanos

Otro tipo en Python es el tipo booleano. Hay dos valores booleanos: verdadero y falso.
Se pueden crear comparando valores, por ejemplo, utilizando el operador igual ==.

"""
print("-----------------------------------------\nBooleanos\n-----------------------------------------")

print()

my_boolean = True
print(my_boolean)


print(2 == 3)

print()

"""
Otro operador de comparación, el operador no igual (! =), Se evalúa como Verdadero si los elementos que se comparan no son iguales, y Falso si lo son.
"""
print("hello" == "hello")

print(2 != 3)

print("Hola" != "Adios")

print()

"""
Python también tiene operadores que determinan si un número (flotante o entero) es mayor o menor que otro. Estos operadores son> y <respectivamente.
"""
print(7>4)
print(32<12)

print()

"""
Los operadores mayor o igual que, y menor o igual a son> = y <=.
Son los mismos que los operadores estrictos mayores y menores que los operadores, excepto que devuelven Verdadero cuando se comparan números iguales.
"""
print(7 <= 8)
print(9 >= 9.0)

print()


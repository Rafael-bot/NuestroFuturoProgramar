"""
Tuples

Las tuplas son muy similares a las listas, excepto que son inmutables (no se pueden cambiar).
Además, se crean usando paréntesis, en lugar de corchetes.
"""
print("-----------------------------------------\nTuplas\n-----------------------------------------")
palabras = ("Tuplas","huevos","Sugar")
print(palabras[0])

print()

"""
Las tuplas se pueden crear sin paréntesis, simplemente separando los valores con comas.
"""
my_tuple = "Tuplas","huevos","Sugar"
print(my_tuple[0])

tuple = (1, (1, 2, 3))
print(tuple[1])
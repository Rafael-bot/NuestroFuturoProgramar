package Introducción.Ejercicio.Clase;

public class clasesRectanguloFormula {
	
	public static double area(double altura, double anchura) {
		
		double resultadoarea = altura*anchura;
		return resultadoarea;
	
	}
	
	public static double perimetro(double altura, double anchura) {
		
		double resultadoperimetro = altura+anchura;
		return resultadoperimetro;
	
	}
	
}
